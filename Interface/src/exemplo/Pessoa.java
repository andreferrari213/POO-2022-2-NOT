package exemplo;

/**
 * Uma classe comum para armazenar dados de Pessoa.
 * 
 * 
 * @author gabriel vieira
 *
 */
public class Pessoa {

	private int id;
	private String nome;
	
	
	public String getNome() {
		return nome;
	}
	
	public void setNome(String nome) {
		this.nome = nome;
	}
	
	public int getId() {
		return id;
	}
	
	public void setId(int id) {
		this.id = id;
	}
}
